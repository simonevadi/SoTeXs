import numpy as np
import matplotlib.pyplot as plt

Au15=np.loadtxt('reflectivity/Au15.dat', skiprows=2)
Pt15=np.loadtxt('reflectivity/Pt15.dat', skiprows=2)
Rh15=np.loadtxt('reflectivity/Rh15.dat', skiprows=2)
Pd15=np.loadtxt('reflectivity/Pd15.dat', skiprows=2)
Rh30_Pt8_15=np.loadtxt('reflectivity/Rh30_Pt8_15.dat', skiprows=2)
Au30_Pt8_15=np.loadtxt('reflectivity/Au30_Pt8_15.dat', skiprows=2)



fig2 = plt.figure(3,figsize=(10,10))
